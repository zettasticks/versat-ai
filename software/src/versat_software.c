typedef struct {
  u32 sign;
  u32 exp;
  u32 frac;
} ExpIndex;

ExpIndex Test(float n, int fractionalPrecision) {
  float data = n;

  int expPrecision = 8;
  int halfExpPrecision = expPrecision - 1;
  int expMax = (1 << halfExpPrecision) - 1;

  int asInteger = (int)data;
  int realPart = asInteger;
  if (realPart >= expMax) {
    realPart = expMax;
  }
  if (realPart <= -expMax) {
    realPart = -expMax;
  }

  bool isNegative = false;
  if (data < 0.0) {
    realPart = -realPart;
    isNegative = true;
    data = -data;
  }

  // NOTE: If the realPart goes outside the [-127,127] range, then
  //       we do not actually care about the fractional part since we are in the
  //       range of infinity anyway.

  // Should be between 0 and 1
  float asFraction = data - (float)realPart;
  u32 mantissaPartU32 = *((u32 *)&asFraction);

  UnpackedFP unpacked = Unpack(asFraction, true);

  u32 test = (unpacked.mantissa >>
              (8 + (16 - fractionalPrecision) - unpacked.exponent)) &
             ((1 << fractionalPrecision) - 1);

  // versat_printf("%d %02x %03x\n",isNegative,realPart,test);

  ExpIndex res = {isNegative ? 1 : 0, realPart, test};
  return res;
}

float InverseTest(ExpIndex in, int fractionalPrecision) {
  u32 fullMantissa = in.frac << (8 + (16 - fractionalPrecision));

  u32 exponent = 0;
  while (fullMantissa >= (1 << 24)) {
    fullMantissa /= 2;
    exponent += 1;
  }
  while (fullMantissa < (1 << 23)) {
    fullMantissa *= 2;
    exponent -= 1;
  }

  // versat_printf("%04x %04x\n",exponent,fullMantissa);

  UnpackedFP res = {};
  res.mantissa = fullMantissa;
  res.exponent = exponent;
  res.sign = in.sign;

  float f = Pack(res);
  return f;
}

