double my_exp(double x) {
  double result = 1.0;
  double term = 1.0;
  double div = 1.0;
  double dx = (double)x;
  int maxTerms = 100000; // Failsafe

  double lastResult = result;
  for (int n = 1; n < maxTerms; n++) {
    term *= dx / div;
    div += 1.0;
    result += term;

    // As term gets smaller eventually we will reach a point where addition does
    // not change anything
    if (lastResult == result) {
      break;
    }
    lastResult = result;
  }

  return result;
}

#define MAX_ITERS 20
double thetah_table[MAX_ITERS] = {
    0.549306, 0.255413, 0.125657, 0.062582, 0.031260, 0.015626, 0.007813,
    0.003906, 0.001953, 0.000977, 0.000488, 0.000244, 0.000122, 0.000061,
    0.000031, 0.000015, 0.000008, 0.000004, 0.000002, 0.000001};

double Cordic_arctanh(double y, double x) {
  double angle = 0.0;
  double P2i = 0.5;
  int k = 4;

  for (int i = 1; i < MAX_ITERS; i++) {
    double arc_tangent = thetah_table[i - 1];

    int iters = 1;
    if (i == k) {
      iters = 2;
      k = (3 * k) + 1;
    }

    for (int k = 0; k < iters; k++) {
      double sigma = 1.0;
      if (y < 0) {
        sigma = -1.0;
      }

      angle = angle + sigma * arc_tangent;
      double calcX = x - sigma * y * P2i;
      double calcY = y - sigma * x * P2i;

      x = calcX;
      y = calcY;
    }

    P2i /= 2.0;
  }

  return angle;
}

#define MATH_E 2.71828182846
#define MATH_PI 3.14159265359

double Cordic_log(double in) {
  double val = in;

  double extra = 0;
  while (val > MATH_E) {
    val /= MATH_E;
    extra += 1;
  }

  while (val < 1.0f) {
    val *= MATH_E;
    extra -= 1;
  }

  double res = (2.0 * Cordic_arctanh(val - 1, val + 1)) + extra;
  return res;
}

double Cordic_exp(double exponent) {
  int k = 4;
  double angle = exponent;
  double y = 1.207497067763;
  double x = 1.207497067763;
  double P2i = 0.5;

  int extra = 0;
  while (angle > 1.0) {
    angle -= 1.0;
    extra += 1;
  }

  while (angle < 0) {
    angle += 1.0;
    extra -= 1;
  }

  for (int i = 1; i < MAX_ITERS; i++) {
    double arc_tangent = thetah_table[i - 1];

    int iters = 1;
    if (i == k) {
      iters = 2;
      k = (3 * k) + 1;
    }

    for (int k = 0; k < iters; k++) {
      double sigma = 1.0;
      if (angle > 0.0) {
        sigma = -1.0;
      }

      angle = angle + sigma * arc_tangent;
      double calcX = x - sigma * y * P2i;
      double calcY = y - sigma * x * P2i;

      x = calcX;
      y = calcY;
    }

    P2i /= 2.0;
  }

  for (int i = 0; i < extra; i++) {
    x *= MATH_E;
  }
  for (int i = 0; i > extra; i--) {
    x /= MATH_E;
  }

  return x;
}

double my_log(double x) {
  double start = 0.0;

  for (int i = 0; i < 1000; i++) {
    double last = start;
    start = start + 2.0 * ((x - my_exp(start)) / (x + my_exp(start)));
    if (last == start) {
      break;
    }
  }

  return start;
}

double my_pow(double b, double e) {
  double result = my_exp(e * my_log(b));
  return result;
}

double Cordic_pow(double b, double e) {
  double result = Cordic_exp(e * Cordic_log(b));
  return result;
}

